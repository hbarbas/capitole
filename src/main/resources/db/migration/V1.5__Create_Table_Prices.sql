CREATE TABLE PRICES (
                        ID INT NOT NULL PRIMARY KEY,
                        BRAND_ID INT REFERENCES BRANDS,
                        START_DATE TIMESTAMP,
                        END_DATE TIMESTAMP,
                        PRODUCT_ID INT REFERENCES PRODUCTS,
                        PRIORITY_ID INT REFERENCES PRIORITIES,
                        PRICE DECIMAL(10, 2),
                        CURRENT_ID INT REFERENCES CURRENCIES
);

